import random

def cow_And_Bull_Game(user_Input):

    
    random_Number = str(random.randrange(1000, 10_000)) # converted this to a string for possible iteration
    print(random_Number)

    #variables to track the cow and bull ccount
    cow_Count = 0
    bull_Count = 0

    for i in range(4):
        if random_Number[i] == user_Input[i]:
            cow_Count += 1
        elif random_Number[i] in user_Input:
            bull_Count += 1

    if cow_Count <= 1:
        cow_plural = 'cow'
    else:
        cow_plural ='cows'
    
    if bull_Count <= 1 :
        bull_plural = 'bull'

    else:
        bull_plural ='bulls'
    

    
    print(f"\n{cow_Count} {cow_plural}  {bull_Count} {bull_plural}\n")
    if cow_Count == 4:
        print("Congratulations on winnning the game!!! ")


while True:
    print("Welcome to cow and Bull's Game >>>\n")

    user_Input = input("Enter a 4 digit number \n>>>")

    if len(user_Input) != 4 or not user_Input.isdigit(): #accounting for edge cases of index error
        print("\ninvalid input! Enter a 4 digit number >>> \n")
        continue

    cow_And_Bull_Game(user_Input) #run the game